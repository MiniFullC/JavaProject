public class TestOperator03 {
	public static void main(String[] args) {
		String a = "3";
		int b = 4;
		int c = 5;
		char d = 'a';
		System.out.println(a+b);
		System.out.println(a+c);
		System.out.println(b+c+a);
		System.out.println(d+4);    //d=97
		}

}
